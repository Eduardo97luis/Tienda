    private void codigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoKeyTyped
        // TODO add your handling code here:
         if(codigo.getText().length()>17 || !isDigit(evt.getKeyChar()))evt.consume();
         if(codigo.getText().length()>3){lineacodigo.setForeground(t);codigo.setForeground(l);cod=true;}
         else {lineacodigo.setForeground(f);codigo.setForeground(f);cod=false;}
         validar();
    }//GEN-LAST:event_codigoKeyTyped
    public void validar() {
        if (cod && nomb && prove && marc && comp && vent) {
            aceptar.setEnabled(true);
        } else {
            aceptar.setEnabled(false);
        }
    }

