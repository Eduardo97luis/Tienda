    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        // TODO add your handling code here:
        String present = seleccion=jComboBox1.getSelectedItem().toString();
        String unicajj = unidadcaja.getText();
        if(present.equals("Unitario"))
            unicajj="1";
        
        String cad = codigo.getText()+",'"+nombre.getText()+"','"+marca.getText()+"',"+prec_comp.getText()+","+prec_vent.getText()+",0";    
        conexion.consultas("insert into portillito.producto values("+cad+")");
        String cad2=codigo.getText()+",'"+present+"',"+unidadcaja.getText();
        conexion.consultas("insert into portillito.presentacion_p values("+cad2+")");

        ResultSet res = conexion.consultas("select * from portillito.proveedor where razonsocial = '"+proveedor.getText()+"'");
        
        try {
            while (res.next()) {
                String id = res.getString("idproveedor");
                conexion.consultas("insert into portillito.catalogo_prod_prov values("+codigo.getText()+","+id+")");
            }
        } catch (Exception e) {
        }
        
        Correcto c;
        Error e;
        if(conexion.getConsulta()){
             c= new Correcto(new javax.swing.JFrame(),true);
             c.setVisible(true);
             this.dispose();
        }
        else{
            e=new Error(new javax.swing.JFrame(),true,"Producto no agregado");
            e.setVisible(true);
        }
         
    }//GEN-LAST:event_aceptarActionPerformed

